int	ft_strncmp(char *s1, char *s2, unsigned int *n)
{
	int				i;
	int				j;
	unsigned int	k;

	i = 0;
	j = 0;
	k = 0;
	while (k < n)
	{
		if ((s1[i] == s2[j]) && s1[i + 1] == '\0' && s2[j + 1] == '\0')
			return (0);
		else if (s1[i] != s2[j])
			return (s1[i] - s2[j]);
		else
		{
			i++;
			j++;
		}
		k++;
	}
	return (0);
}
